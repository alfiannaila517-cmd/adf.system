if ($isProduction) {
    error_reporting(0);
    ini_set('display_errors', 0);
    ini_set('log_errors', 1);
} else {
    error_reporting(E_ALL);
    ini_set('display_errors', 1);
}

if (!defined('CURRENCY_SYMBOL')) define('CURRENCY_SYMBOL', 'Rp');
if (!defined('CURRENCY_DECIMAL')) define('CURRENCY_DECIMAL', 0);

