if (php_sapi_name() === 'cli') {
    die("Script ini hanya bisa dijalankan via browser, bukan CLI\n");
}

// Security: hanya untuk admin, cek IP atau token
$allowedIPs = ['127.0.0.1', 'localhost', '::1'];
$clientIP = $_SERVER['REMOTE_ADDR'] ?? '';
$isLocalhost = in_array($clientIP, $allowedIPs);
$token = $_GET['token'] ?? '';
$validToken = 'pwf_setup_2026'; // Change ini ke token unik

if (!$isLocalhost && $token !== $validToken) {
    die("⛔ Access Denied. Jalankan dari localhost atau gunakan token yang valid.");
}

echo "<h1>🚀 PWF PO System - Auto Setup</h1>";
echo "<pre>";

$success = true;
$targetDir = '/home/adfb2574/public_html/pwf-po-system';
$configPath = $targetDir . '/config/config.php';
$databasePath = $targetDir . '/database.sql';

// ===== STEP 1: Create config.php =====
echo "\n[STEP 1] Creating config.php...\n";

$configContent = <<<'CONFIG'
<?php
/**
 * PWF PO System - Configuration File
 * Auto-generated by setup script
 */

if (!ob_get_level()) {
    ob_start();
}

if (!defined('SESSION_NAME')) define('SESSION_NAME', 'PWF_SESSION');
if (!defined('SESSION_LIFETIME')) define('SESSION_LIFETIME', 3600 * 8);

if (session_status() === PHP_SESSION_NONE) {
    session_name(SESSION_NAME);
    $isSecure = (!empty($_SERVER['HTTPS']) && $_SERVER['HTTPS'] !== 'off');
    session_set_cookie_params([
        'lifetime' => SESSION_LIFETIME,
        'path' => '/',
        'secure' => $isSecure,
        'httponly' => true,
        'samesite' => 'Lax'
    ]);
    session_start();
}

defined('APP_ACCESS') or define('APP_ACCESS', true);

if (!defined('APP_NAME')) define('APP_NAME', 'PWF PO System - Purchase Order Management');
if (!defined('APP_VERSION')) define('APP_VERSION', '1.0.0');
if (!defined('APP_YEAR')) define('APP_YEAR', '2026');
if (!defined('DEVELOPER_NAME')) define('DEVELOPER_NAME', 'Ariefsystemdesign.net');
if (!defined('DEVELOPER_LOGO')) define('DEVELOPER_LOGO', 'assets/img/developer-logo.png');

$isProduction = (strpos($_SERVER['HTTP_HOST'] ?? '', 'localhost') === false && 
                strpos($_SERVER['HTTP_HOST'] ?? '', '127.0.0.1') === false);

if ($isProduction) {
    if (!defined('DB_HOST')) define('DB_HOST', 'localhost');
    if (!defined('DB_NAME')) define('DB_NAME', 'adfb2574_pwf_po_system');
    if (!defined('DB_USER')) define('DB_USER', 'adfb2574_pwf_user');
    if (!defined('DB_PASS')) define('DB_PASS', '@Nnoc2025');
} else {
    if (!defined('DB_HOST')) define('DB_HOST', 'localhost');
    if (!defined('DB_NAME')) define('DB_NAME', 'pwf_po_system');
    if (!defined('DB_USER')) define('DB_USER', 'root');
    if (!defined('DB_PASS')) define('DB_PASS', '');
}
if (!defined('DB_CHARSET')) define('DB_CHARSET', 'utf8mb4');

if (!defined('BASE_PATH')) define('BASE_PATH', dirname(dirname(__FILE__)));

$host = $_SERVER['HTTP_HOST'] ?? 'localhost';
$port = $_SERVER['SERVER_PORT'] ?? '80';
$protocol = (!empty($_SERVER['HTTPS']) && $_SERVER['HTTPS'] !== 'off') ? 'https' : 'http';

if (!defined('BASE_URL')) {
    if (php_sapi_name() !== 'cli') {
        $scriptDir = dirname($_SERVER['SCRIPT_NAME'] ?? '');
        $basePath = '';
        
        if (strpos($_SERVER['HTTP_HOST'] ?? '', 'localhost') !== false || 
